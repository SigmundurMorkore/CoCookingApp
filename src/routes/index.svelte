			<h2>Directions</h2>
			<ol>
				<li>Bring a large pot of water to a boil, and salt generously.</li>
				<li>
					In a large frying pan render out the guanciale, with a little bit of vegetable oil, on low
					heat until the guanciale is crispy. Add in the black pepper and briefly toast. Add in a
					splash of the pasta water to stop the cooking process, and keep aside.
				</li>
				<li>
					Cook the pasta until al dente, or roughly 3 minutes less than the package instructions.
				</li>
				<li>
					Once the pasta is cooked add it to the pan, reserving 1 cup of the pasta water, over low
					heat and toss to coat the pasta in the guanciale.
				</li>
				<li>
					While tossing the pasta, sprinkle in 200g of the Pecorino Romano, to bring the sauce
					together,and coat the pasta. If there is too much liquid, add more cheese to thicken, if
					the cheese is clumping or too thick, add some of the reserved pasta water.
				</li>
				<li>Plate the pasta, and garnish with the remaining 50g Pecorino Romano.</li>
			</ol>
